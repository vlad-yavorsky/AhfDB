package ua.org.ahf.ahfdb.model;

public class Oblast {

    private Long id;
    private String name;

    public Oblast(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
